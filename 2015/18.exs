defmodule AOC18 do
  def part1(input) do
    grid = input
    |> String.split("\n", trim: true)
    |> Enum.map(&String.split(&1,"", trim: true))
    Enum.reduce(1..100, grid, fn(_step, grid) -> next(grid) end)
    |> List.flatten()
    |> Enum.filter(fn(e)-> e == "#" end)
    |> Enum.count()
  end

  def part2(input) do

    grid = input
    |> String.split("\n", trim: true)
    |> Enum.map(&String.split(&1,"", trim: true))

    size_y = length(grid) - 1
    size_x = (grid |> hd() |> length()) - 1

    Enum.reduce(1..100, grid, fn(_step, grid) ->
      grid
      |> next()
      |> update_at(0, 0, fn(_)-> "#" end)
      |> update_at(size_x, 0, fn(_)-> "#" end)
      |> update_at(0, size_y, fn(_)-> "#" end)
      |> update_at(size_x, size_y, fn(_)-> "#" end)

    end)
    |> List.flatten()
    |> Enum.filter(fn(e)-> e == "#" end)
    |> Enum.count()
  end

  def next(input) do
    input
    |> Enum.with_index()
    |> Enum.map(fn({line, y}) ->
      line
      |> Enum.with_index()
      |> Enum.map(fn({el, x}) ->
        n = is_on?(input, x + 1, y) + is_on?(input, x - 1, y) + is_on?(input, x, y + 1) + is_on?(input, x, y - 1) +
        is_on?(input, x + 1, y + 1) + is_on?(input, x - 1, y + 1) + is_on?(input, x + 1, y - 1) + is_on?(input, x - 1, y - 1)
        new_value = "."
        new_value = if ((n == 2) or (n == 3)) do
          if (el == "#"), do: "#", else: "."
        else
          new_value
        end
        new_value = if (n == 3) do
          if (el == "."), do: "#", else: new_value
        else
          new_value
        end
        {x, y, n, el, new_value}
        new_value

      end)
    end)
  end

  def is_on?(_input, x, y) when x < 0 or y < 0, do: 0
  def is_on?(input, x, y) do
    v = (Enum.at(input, y, [])
    |> Enum.at(x, "."))
    if v == "#", do: 1, else: 0
  end

  def update_at(input, x, y, _update_fn) when x < 0 or y < 0, do: input
  def update_at(input, x, y, update_fn) when y < length(input) do
    if x >= (Enum.at(input, y) |> length) do input
    else
      List.update_at(input, y, fn(line) -> List.update_at(line, x, update_fn) end)
    end
  end
  def update_at(input, x, y, update_fn) do
    List.update_at(input, y, fn(line) -> List.update_at(line, x, update_fn) end)
  end
end

