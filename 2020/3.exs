input = input
|> String.split("\n")
|> Enum.map(&(String.split(&1,"", trim: true)))

get = fn(input, x, y, l) ->
  input
  |> Enum.at(y)
  |> Enum.at(rem(x, l))
end

l = input |> Enum.at(0) |> length()

n = length(input) - 1


{_, a} = Enum.reduce(0..n, {0, 0}, fn(y, {x, count}) ->
  if get.(input, x, y, l) == "#", do: {x + 1, count + 1}, else: {x + 1, count}
end)
|> IO.inspect()

{_, b} = Enum.reduce(0..n, {0, 0}, fn(y, {x, count}) ->
  if get.(input, x, y, l) == "#", do: {x + 3, count + 1}, else: {x + 3, count}
end)
|> IO.inspect()

{_, c} = Enum.reduce(0..n, {0, 0}, fn(y, {x, count}) ->
  if get.(input, x, y, l) == "#", do: {x + 5, count + 1}, else: {x + 5, count}
end)
|> IO.inspect()

{_, d} = Enum.reduce(0..n, {0, 0}, fn(y, {x, count}) ->
  if get.(input, x, y, l) == "#", do: {x + 7, count + 1}, else: {x + 7, count}
end)
|> IO.inspect()

{_, _ ,e} = Enum.reduce(0..(div(n, 2)), {0, 0 ,0}, fn(_, {x, y, count}) ->
  if get.(input, x, y, l) == "#", do: {x + 1, y + 2, count + 1}, else: {x + 1, y + 2, count}
end)
|> IO.inspect()

IO.puts(a * b * c * d * e)
